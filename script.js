const next = (evt) => {
	const data = document.getElementsByClassName('slide');
	let slide = (parseInt(data[0].getAttribute('data')) + 1);
	let title = document.getElementById('box-title');

	const next = document.getElementById('next');
	const prev = document.getElementById('prev');

	if (slide == 1) {
		prev.removeAttribute('disabled');
	} else if (slide == 10) {
		next.setAttribute('disabled', true);
	}

	data[0].setAttribute('data', slide);
	title.innerHTML = slide;

	char = ['A', 'B', 'C', 'D', 'E', 'F', 'G'];
	int = 17;
	let slideChange = 'S'+slide;
	slideChange = eval(slideChange+'()');

	for (let y = 0; y < char.length; y++) {
		botLine = char[y];
		dataLine = slideChange[char[y]];
		dataLineImg = false;

		if (slide == 10) {
			dataLineImg = slideChange[char[y]+char[y]];
		}

		for (var x = 0; x < int; x++) {
			idBox = document.getElementById(botLine+(x+1));

			if (dataLine[x]) {
				if (dataLineImg) {
					idBox.setAttribute('style', 'background: url("'+dataLineImg[x]+'"); background-size: cover;');
				} else {
					idBox.setAttribute('style', 'background: #fff;');
				}
			} else {
				idBox.setAttribute('style', 'background: #0e07df;');
			}
			
		}
	}	
}

